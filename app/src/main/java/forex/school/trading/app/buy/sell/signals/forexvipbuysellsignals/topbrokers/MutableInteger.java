package forex.school.trading.app.buy.sell.signals.forexvipbuysellsignals.topbrokers;

public class MutableInteger {
    public int value;

    public MutableInteger(int i) {
        this.value = i;
    }

    public int getValue() {
        return this.value;
    }

    public void setValue(int i) {
        this.value = i;
    }
}
